
#################################################################################################################################################

# Imports
import os
import requests

#################################################################################################################################################

# Get Address
remote = os.getenv('ODBC_NETWORK_ADDRESS')

#################################################################################################################################################

# Laminador
class laminador:

    def rfa():
        res = requests.get(
            url=f'{remote}/laminador/rfa/',
        )
        res.raise_for_status()
        return res.json()

    def rfal2():
        res = requests.get(
            url=f'{remote}/laminador/rfal2/',
        )
        res.raise_for_status()
        return res.json()

