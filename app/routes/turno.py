
#################################################################################################################################################

# Imports
import json
import flask
import py_misc

# modules
from .. import turno

#################################################################################################################################################

Request = flask.Request
Response = flask.Response

#################################################################################################################################################

# Load Routes
def __load__(app: py_misc.Express):

    #################################################################################################################################################

    @app.route('/api/turma/')
    def turmaTurno(req: Request, res: Response):
        data = turno.escala.get()
        return res(
            json.dumps(data),
            mimetype='application/json',
            status=200
        )

