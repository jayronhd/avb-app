import os
import py_misc

# Routes
from . import routes

##########################################################################################################################

# Declare HTTP API
app = py_misc.Express()

# Set API Port
app.port(
    int(os.getenv('AVB_APP_PORT'))
)

# Load Routes
routes.__load__(app)

# Start Server
app.start()

# Keep Main Thread Alive
py_misc.keepalive()

