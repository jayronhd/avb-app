
#################################################################################################################################################

# Imports
import py_misc

# modules
from .. import turno

#################################################################################################################################################

Request = py_misc.flask.request
Response = py_misc.flask.Response

#################################################################################################################################################

# Load Routes
def __load__(api: py_misc.API):

    #################################################################################################################################################

    @api.route('/api/turma/')
    def turma_turno(req: Request, res: Response):
        data = turno.escala.get()
        return res(
            py_misc.json.dumps(data),
            mimetype='application/json',
            status=200
        )

