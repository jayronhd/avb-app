SELECT
   	cte00._filename,
   	cte00._date,
   	ROUND((cte16.TIME_TRF_01_SHIFT) / 864, 1) AS M1,
   	ROUND((cte00.TIME_TRF_01_SHIFT) / 288, 1) AS M1_00,
   	ROUND((cte08.TIME_TRF_01_SHIFT - cte00.TIME_TRF_01_SHIFT) / 288, 1) AS M1_08,
   	ROUND((cte16.TIME_TRF_01_SHIFT - cte08.TIME_TRF_01_SHIFT) / 288, 1) AS M1_16,
   	ROUND((cte16.TIME_TRF_02_SHIFT) / 864, 1) AS M2,
   	ROUND((cte00.TIME_TRF_02_SHIFT) / 288, 1) AS M2_00,
   	ROUND((cte08.TIME_TRF_02_SHIFT - cte00.TIME_TRF_02_SHIFT) / 288, 1) AS M2_08,
   	ROUND((cte16.TIME_TRF_02_SHIFT - cte08.TIME_TRF_02_SHIFT) / 288, 1) AS M2_16,
   	ROUND((cte16.TIME_TRF_03_SHIFT) / 864, 1) AS M3,
   	ROUND((cte00.TIME_TRF_03_SHIFT) / 288, 1) AS M3_00,
   	ROUND((cte08.TIME_TRF_03_SHIFT - cte00.TIME_TRF_03_SHIFT) / 288, 1) AS M3_08,
   	ROUND((cte16.TIME_TRF_03_SHIFT - cte08.TIME_TRF_03_SHIFT) / 288, 1) AS M3_16,
   	ROUND((cte16.TIME_TRF_04_SHIFT) / 576, 1) AS M4,
   	ROUND((cte00.TIME_TRF_04_SHIFT) / 192, 1) AS M4_00,
   	ROUND((cte08.TIME_TRF_04_SHIFT - cte00.TIME_TRF_04_SHIFT) / 192, 1) AS M4_08,
   	ROUND((cte16.TIME_TRF_04_SHIFT - cte08.TIME_TRF_04_SHIFT) / 192, 1) AS M4_16,
   	ROUND((cte16.TIME_TRF_05_SHIFT) / 864, 1) AS M5,
   	ROUND((cte00.TIME_TRF_05_SHIFT) / 288, 1) AS M5_00,
	ROUND((cte08.TIME_TRF_05_SHIFT - cte00.TIME_TRF_05_SHIFT) / 288, 1) AS M5_08,
    ROUND((cte16.TIME_TRF_05_SHIFT - cte08.TIME_TRF_05_SHIFT) / 288, 1) AS M5_16
FROM
    (SELECT * FROM _sort_trf_util_2 WHERE TIME_PLC_TRF_SHIFT = 28800) AS cte00,
    (SELECT * FROM _sort_trf_util_2 WHERE TIME_PLC_TRF_SHIFT = 57600) AS cte08,
    (SELECT * FROM _sort_trf_util_2 WHERE TIME_PLC_TRF_SHIFT = 86400) AS cte16
WHERE (
    (cte00._date = cte08._date) AND
    (cte00._date = cte16._date)
)
ORDER BY cte00._date DESC
